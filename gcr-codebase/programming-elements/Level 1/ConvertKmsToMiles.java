public class ConvertKmsToMiles{
  public static void main(String[] args){

      // Assigned input.
      double distanceInKms = 10.8;

      // Converting kilometers into miles.
      double distanceInMiles = 1.6 * distanceInKms;

      // Display the result.
      System.out.println("The distance "+ distanceInKms+" km in miles is "+ distanceInMiles);
  }
}